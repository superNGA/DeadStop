namespace Decoder::Inst
{

struct REX_t
{
    REX_t() : m_iByte(0) {}


    ///////////////////////////////////////////////////////////////////////////
    ///////////////////////////////////////////////////////////////////////////
    inline bool IsREXValid()
    {
        // The upper 4 bits must be 0100.
        return (m_iByte & 0b11110000) == 0b01000000;
    }


    ///////////////////////////////////////////////////////////////////////////
    ///////////////////////////////////////////////////////////////////////////
    inline void Clear()
    {
        m_iByte = 0;
    }


    ///////////////////////////////////////////////////////////////////////////
    ///////////////////////////////////////////////////////////////////////////
    inline void Store(Byte iByte)
    {
        m_iByte = iByte;
    }


    Byte m_iByte;
};

}
